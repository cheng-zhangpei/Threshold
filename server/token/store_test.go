package token

import (
	"os"
	"path/filepath"
	"testing"
	"time"

	"Threshold/pkg/storage"
)

func setupTokenStore(t *testing.T) (*Store, func()) {
	t.Helper()
	dir := t.TempDir()
	dbPath := filepath.Join(dir, "test.db")
	store, err := storage.NewBoltStore(dbPath)
	if err != nil {
		t.Fatalf("NewBoltStore: %v", err)
	}
	tokenStore, err := NewStore(store, filepath.Join(dir, "keys"))
	if err != nil {
		t.Fatalf("NewStore: %v", err)
	}
	cleanup := func() {
		store.Close()
		os.RemoveAll(dir)
	}
	return tokenStore, cleanup
}

func TestToken_Generate_Success(t *testing.T) {
	s, cleanup := setupTokenStore(t)
	defer cleanup()

	token, expiresAt, err := s.Generate("admin", 1*time.Hour)
	if err != nil {
		t.Fatalf("Generate: %v", err)
	}
	if token == "" {
		t.Fatal("expected non-empty token")
	}
	if expiresAt <= time.Now().UnixMilli() {
		t.Fatal("expires_at should be in the future")
	}
}

func TestToken_Validate_Success(t *testing.T) {
	s, cleanup := setupTokenStore(t)
	defer cleanup()

	tokenStr, _, err := s.Generate("admin", 1*time.Hour)
	if err != nil {
		t.Fatalf("Generate: %v", err)
	}

	entry, err := s.Validate(tokenStr)
	if err != nil {
		t.Fatalf("Validate: %v", err)
	}
	if entry.Token != tokenStr {
		t.Errorf("token mismatch: got %s", entry.Token)
	}
	if entry.Username != "admin" {
		t.Errorf("username mismatch: got %s", entry.Username)
	}
}

